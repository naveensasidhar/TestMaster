# True Scenario - Code Block is Executed
temperature = 95
if temperature > 80:
    print("It's too hot!")
    print("Stay Inside")

# false Scenario - Code Block Won't Execute
temperature = 75
if temperature > 80:
    print("It's too Hot!")
    print("Stay Inside") 
else:
    print("Enjoy Outside!")      
# Part Of Code Block    
print("Have A Good Day") 

# Else If Condition
temperature = 59
if temperature > 80:
    print("Too Hot!!")
    print("Stay Inside Only")
elif temperature < 60:
    print("Looks Like It's Cold")
    print("Too cold say Inside")
else:
    print("Good, Let's Go Outside")        

# OR Condition
temparature = 75
if temperature > 80 or temparature < 60:
    print("Stay Alway's Inside")
else:
    print("Enjoy The Day Outside")      